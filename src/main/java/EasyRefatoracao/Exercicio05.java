package EasyRefatoracao;

public class Exercicio05 {


    public int soma (int numero1, int numero2, int numero3){
        int soma;
        soma = numero1+numero2+numero3;
        return soma;

    }
 

    public int subitracao (int numero1, int numero2, int numero3){
        int subitracao;

        subitracao=numero1-numero2-numero3;
        return subitracao;
        
    }


    public int multiplicacao (int numero1, int numero2, int numero3){
        int multiplicacao;

        multiplicacao=numero1*numero2*numero3;
        return multiplicacao;
        
    }
    

    public int media (int numero1, int numero2, int numero3){
        int media;
        media = (numero1+numero2+numero3)/3;
        return media;
        
    }
}
