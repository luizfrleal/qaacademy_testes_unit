package Metodos;

public class ExercicioMetosoSemRetorno {

    public static void main(String[] args) {
        nome();
        sobrenome();
        pai();
        mae();
        irmao();
    }

    public static void nome() {
        System.out.println("Luiz");

    }

    public static void sobrenome() {
        System.out.println("Leal");

    }

    public static void pai() {
        System.out.println("jose");

    }

    public static void mae() {
        System.out.println("Ana");
    }

    public static void irmao() {
        System.out.println("Marcus");

    }

}
